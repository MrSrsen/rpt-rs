//! L0.5 — the payload cipher.
//!
//! The compressed `Contents` payload is encrypted with a **modified AES-128 in CFB-128
//! mode**. It is *not* standard AES:
//!
//! - standard AES S-box and T-table values, but a **transposed state convention** (the
//!   round mixes input bytes `in[3], in[6], in[9], in[12]` into output word 0), and
//! - a **non-standard key schedule** — so the 44 round-key words below are the engine's
//!   expansion of the universal fixed key (constant for every fixed-key file).

