/// The four AES T-tables, derived once from the S-box (`Te0[x] = mul2(s).s.s.mul3(s)`).
struct Tables {
    te: [[u32; 256]; 4],
}

fn xtime(a: u8) -> u8 {
    (a << 1) ^ if a & 0x80 != 0 { 0x1b } else { 0x00 }
}

fn tables() -> &'static Tables {
    use std::sync::OnceLock;
    static T: OnceLock<Tables> = OnceLock::new();
    T.get_or_init(|| {
        let mut te0 = [0u32; 256];
        for (x, slot) in te0.iter_mut().enumerate() {
            let s = SBOX[x];
            let s2 = xtime(s);
            let s3 = s2 ^ s;
            *slot = (s2 as u32) << 24 | (s as u32) << 16 | (s as u32) << 8 | s3 as u32;
        }
        let mut te = [[0u32; 256]; 4];
        te[0] = te0;
        for x in 0..256 {
            te[1][x] = te0[x].rotate_right(8);
            te[2][x] = te0[x].rotate_right(16);
            te[3][x] = te0[x].rotate_right(24);
        }
        Tables { te }
    })
}

/// The modified-AES-128 block **encryption** (used by CFB to make the keystream).
pub(crate) fn encrypt_block(input: &[u8; 16]) -> [u8; 16] {
    let t = tables();
    let (te0, te1, te2, te3) = (&t.te[0], &t.te[1], &t.te[2], &t.te[3]);
    let mut a = u32::from_le_bytes([input[0], input[1], input[2], input[3]]) ^ RK[0];
    let mut b = u32::from_le_bytes([input[4], input[5], input[6], input[7]]) ^ RK[1];
    let mut c = u32::from_le_bytes([input[8], input[9], input[10], input[11]]) ^ RK[2];
    let mut d = u32::from_le_bytes([input[12], input[13], input[14], input[15]]) ^ RK[3];

    for r in 1..10 {
        let k = 4 * r;
        let na = te0[(a >> 24) as usize]
            ^ te1[(b >> 16) as usize & 0xff]
            ^ te2[(c >> 8) as usize & 0xff]
            ^ te3[d as usize & 0xff]
            ^ RK[k];
        let nb = te0[(b >> 24) as usize]
            ^ te1[(c >> 16) as usize & 0xff]
            ^ te2[(d >> 8) as usize & 0xff]
            ^ te3[a as usize & 0xff]
            ^ RK[k + 1];
        let nc = te0[(c >> 24) as usize]
            ^ te1[(d >> 16) as usize & 0xff]
            ^ te2[(a >> 8) as usize & 0xff]
            ^ te3[b as usize & 0xff]
            ^ RK[k + 2];
        let nd = te0[(d >> 24) as usize]
            ^ te1[(a >> 16) as usize & 0xff]
            ^ te2[(b >> 8) as usize & 0xff]
            ^ te3[c as usize & 0xff]
            ^ RK[k + 3];
        a = na;
        b = nb;
        c = nc;
        d = nd;
    }

    // Final round: SubBytes (no MixColumns), same rotation pattern.
    let fin = |x0: u32, x1: u32, x2: u32, x3: u32, k: u32| -> u32 {
        ((SBOX[(x0 >> 24) as usize] as u32) << 24
            | (SBOX[(x1 >> 16) as usize & 0xff] as u32) << 16
            | (SBOX[(x2 >> 8) as usize & 0xff] as u32) << 8
            | SBOX[x3 as usize & 0xff] as u32)
            ^ k
    };
    let oa = fin(a, b, c, d, RK[40]);
    let ob = fin(b, c, d, a, RK[41]);
    let oc = fin(c, d, a, b, RK[42]);
    let od = fin(d, a, b, c, RK[43]);

    let mut out = [0u8; 16];
    out[0..4].copy_from_slice(&oa.to_le_bytes());
    out[4..8].copy_from_slice(&ob.to_le_bytes());
    out[8..12].copy_from_slice(&oc.to_le_bytes());
    out[12..16].copy_from_slice(&od.to_le_bytes());
    out
}

/// Decrypt `ciphertext` in CFB-128 mode (keystream block = `E(prev_ciphertext)`, first
/// block = `E(iv)`). Returns the plaintext (same length).
pub(crate) fn cfb_decrypt(iv: &[u8; 16], ciphertext: &[u8]) -> Vec<u8> {
    let mut out = Vec::with_capacity(ciphertext.len());
    let mut feedback = *iv;
    for chunk in ciphertext.chunks(16) {
        let ks = encrypt_block(&feedback);
        for (i, &c) in chunk.iter().enumerate() {
            out.push(c ^ ks[i]);
        }
        if chunk.len() == 16 {
            feedback.copy_from_slice(chunk);
        }
        // a partial final chunk ends the stream; no further feedback needed.
    }
    out
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn block_encrypt_known_answer() {
        // Known-answer vector: E(IV) → expected output.
        let iv = [
            0x0d, 0x31, 0x92, 0x7f, 0x9e, 0xe3, 0xa7, 0xac, 0x12, 0xd9, 0x1f, 0x68, 0xd6, 0x6b,
            0x7e, 0x16,
        ];
        let expect = [
            0x8a, 0xba, 0xad, 0x84, 0x22, 0x09, 0xad, 0xa4, 0x73, 0x1e, 0xdb, 0xb9, 0x36, 0x54,
            0x76, 0xd0,
        ];
        assert_eq!(encrypt_block(&iv), expect);
    }
}
